
public class aula05_cont_tiposPrimitivosEOperadores {
	public static void main(String[] args){
		/***
		 * Operators 
		 * ++ -> increment
		 * -- -> decrement
		 * + -> plus
		 * - -> minus
		 * () -> cast
		 * ! -> not
		 * * -> mult
		 * (\) -> division
		 */
		
		int numero1 = 10;
		int numero2 = 20;
		
		System.out.println(numero1 + numero2);
		System.out.println("A soma é: " + (numero1 + numero2));
		System.out.println("A mult é: " + (numero1 * numero2));
		System.out.println("A divisão é: " + (numero1 / numero2));
		System.out.println("a sub é: " + (numero1 - numero2));
	}

}
